$('.bzgl,.lgb').click(function(){
	$.ajax({
		type:"get",
		url:"/Manager/Standard/lgb",
		dataType:"html",
		success:function(ret){
			if(ret == 400){
				reset();
			}else{
				$('.content').html(ret);				
			}
		}
	});
})

